# Python Casting
print (" \n____________Python Casting______________ \n")


# Int type

x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3

print(x,y,z)

# float type 

x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2

print (x,y,z,w)

# string type 

z = str("3")   # z will be '3.0'
x = str(1)     # x will be '1.0'
y = str(2.8)   # y will be '2.8'
w = str("4.2") # w will be '4.2'

print (x,y,z,w)